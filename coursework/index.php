  // Simulated function to fetch global rankings 
  function fetchGlobalRankings() {
    return [10, 5, 20, 15, 8, 12, 3, 18, 7, 1, 25];
  }

  // Function to create a team member element with global ranking
  function createTeamMemberElement(memberIndex, globalRanking) {
    const li = document.createElement("li");
    const a = document.createElement("a");
    const figure = document.createElement("figure");
    const img = document.createElement("img");
    const ionIcon = document.createElement("ion-icon");

    img.src = `../coursework/xmax/assets/images/team-member-${memberIndex + 1}.png`;
    img.alt = "Team member image";

    ionIcon.name = "link-outline";

    figure.appendChild(img);
    a.appendChild(figure);
    a.appendChild(ionIcon);

    li.appendChild(a);

    // Add global ranking text
    const rankText = document.createTextNode(`Rank: ${globalRanking}`);
    li.appendChild(rankText);

    return li;
  }

  // Function to display all members with global rankings
  function displayAllMembers() {
    const leaderboard = document.getElementById("leaderboard");

    // Fetch global rankings
    const globalRankings = fetchGlobalRankings();

    // Display team members with global rankings
    globalRankings.forEach((ranking, index) => {
      const teamMemberElement = createTeamMemberElement(index, ranking);
      leaderboard.appendChild(teamMemberElement);
    });
  }

  // Function to view all members 
  function viewAllMembers() {
  
    //display all members with global rankings
    displayAllMembers();
  }

  // Initial display 
  displayAllMembers();
</script>

